public class Conditional {
    public static void main(String[] args) {
        int a = 7;
        if(a>3){
            System.out.println("a is greater than 3");
        }
        else if(a<3){
            System.out.println(a);
        }
        else{
            System.out.println("a is is 7");
        }
        System.out.println("end of block");
        }
}
